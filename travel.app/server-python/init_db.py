    cursor.execute('CREATE INDEX IF NOT EXISTS idx_h_type ON hostels (type)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_h_price ON hostels (price)')
    cursor.execute('CREATE INDEX IF NOT EXISTS idx_h_rating ON hostels (rating)')

